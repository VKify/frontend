/**
 * Получить версию по номеру
 * @param {string} version - номер версии (например, '1.3.0')
 * @returns {object|undefined} - объект версии или undefined
 */
export function getVersionBySlug(version) {
  return changelog.find(v => v.version === version)
}

/**
 * Получить последнюю версию
 * @returns {object} - объект последней версии
 */
export function getLatestVersion() {
  return changelog[0]
}

/**
 * Получить номер последней версии
 * @returns {string} - номер версии (например, '1.3.0')
 */
export function getLatestVersionNumber() {
  return changelog[0]?.version || '1.0.0'
}

/**
 * Получить все версии
 * @returns {array} - массив всех версий
 */
export function getAllVersions() {
  return changelog
}

/**
 * Получить версии по типу изменений
 * @param {string} type - тип изменений ('new', 'fixed', 'improved')
 * @returns {array} - отфильтрованный массив версий
 */
export function getVersionsByChangeType(type) {
  return changelog.filter(v => v.changes[type]?.length > 0)
}